package com.syntax.class07;

public class Homework2 {

	public static void main(String[] args) {
		// 2. Print numbers from 100 to 1
		int num=100;
		while(num>=1) {
			System.out.print(num+" ");
			num--;
		}
		System.out.println("---------");
		
		for(num=100;num>=1;num--) {
			System.out.print(num+" ");
		}
	}

}
